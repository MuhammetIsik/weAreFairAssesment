    public static double interestSum(double amount){
        String amou =""+ (amount * 0.35 / 365 * 30);
        double realAmount= 0;
        for (int i = 0 ; i< amou.length(); i++){
            if(amou.charAt(i) == '.'){
                realAmount = Double.parseDouble(amou.substring(0,i+3));
            }
        }
        return realAmount ;

    }
